let first = Number(prompt("Input first number"));
let sigh = prompt("Input sigh");
let second = Number(prompt("Input second number"));

if(sigh == "+") alert(first+second);
else if(sigh == "-") alert(first - second);
else if(sigh == "*") alert(first * second);
else if(sigh == "/") alert(first / second);
else if(sigh == "**") alert(first ** second);
else if(sigh == "%") alert(first % second);